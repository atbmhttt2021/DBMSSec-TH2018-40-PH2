);
/
CREATE TABLE CHITIETDONTHUOC(
ID_DONTHUOC VARCHAR2(50),
ID_THUOC VARCHAR2(50),
SOLUONG NUMBER (9),
LIEUDUNG VARCHAR(20),
MOTA NVARCHAR2(100),
CONSTRAINT CT_DONTHUOC_PK PRIMARY KEY(ID_DONTHUOC,ID_THUOC),
