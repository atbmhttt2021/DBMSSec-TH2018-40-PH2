CONSTRAINT FK_THUOC_CTDONTHUOC FOREIGN KEY (ID_THUOC) REFERENCES THUOC(ID_THUOC)
);
/
CREATE TABLE HOADON(
ID_HOADON VARCHAR2(50),
ID_KHAMBENH VARCHAR2(50),
NGAYHD DATE,
NGUOIPT VARCHAR2(50),
